//Estrutura de repetição com texte logico no inicio
console.log('Estrutura de repetição com while no inicio:')
var c = 1
while (c < 10){
    console.log(`Passo ${c}`)
    c++ // c = c + 1
}

//Execulta o bloco e depois texta
console.log('Estrutura de repetição com while depois de um bloco:')
var b = 1
do {//do: faça
    console.log(`Passo ${b}`)
    b++
} while (b <= 6)


//do->(faça), while->(enquanto)
